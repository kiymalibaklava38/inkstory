type T = typeof translations.en
const LangCtx = createContext<{ lang: Lang; t: T; setLang: (l: Lang) => void }>({
  lang: 'en', t: translations.en, setLang: () => {},
})

export function LangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en')

  useEffect(() => {
    const saved = localStorage.getItem('inkstory-lang') as Lang | null
    if (saved === 'tr' || saved === 'en') {
      setLangState(saved)
    } else {
      // Auto-detect Turkish browser
      const browserLang = navigator.language.toLowerCase()
      if (browserLang.startsWith('tr')) setLangState('tr')
    }
  }, [])

  const setLang = (l: Lang) => {
    setLangState(l)
    localStorage.setItem('inkstory-lang', l)
  }

  return (
    <LangCtx.Provider value={{ lang, t: translations[lang], setLang }}>
      {children}
    </LangCtx.Provider>
  )
}

export const useLang = () => useContext(LangCtx)
